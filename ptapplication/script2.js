function toggleHelp() {
    var helpContent = document.getElementById("ptapplication");

    // helpContent이 보이면 숨기고, 숨겨져 있으면 보이게 함
    if (helpContent.style.display === "none" || helpContent.style.display === "") {
        helpContent.style.display = "block";  // 내용 표시
    } else {
        helpContent.style.display = "none";   // 내용 숨기기
    }
}

